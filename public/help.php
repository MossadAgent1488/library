<h1>Справочная информация</h1>

<p>
Данный сайт представляет собой автоматизированную информационную систему
управления библиотекой. Система предназначена для учёта книжного фонда,
читателей и операций выдачи и возврата книг.
</p>

<hr>

<h2>Пользователи системы</h2>

<p>В системе предусмотрено разделение ролей:</p>

<ul>
    <li><strong>Читатель</strong> — может просматривать каталог книг,
        отправлять запросы на выдачу книг и отслеживать свои текущие выдачи.</li>
    <li><strong>Администратор (библиотекарь)</strong> — подтверждает или
        отклоняет запросы на выдачу книг, регистрирует возвраты,
        управляет справочниками и формирует отчёты.</li>
</ul>

<hr>

<h2>Работа с книгами</h2>

<ul>
    <li>Все книги хранятся в электронном каталоге.</li>
    <li>Каждая книга относится к определённому автору и жанру.</li>
    <li>Книга может находиться в состоянии <em>доступна</em> или <em>выдана</em>.</li>
</ul>

<hr>

<h2>Выдача книг</h2>

<p>
Процесс выдачи книг реализован в виде системы заявок:
</p>

<ol>
    <li>Читатель отправляет запрос на выдачу выбранной книги.</li>
    <li>Администратор рассматривает заявку и принимает решение.</li>
    <li>При одобрении книги оформляется выдача с указанием сроков возврата.</li>
</ol>

<p>
При наличии просроченных книг читатель может быть автоматически добавлен
в чёрный список.
</p>

<hr>

<h2>Отчёты</h2>

<p>
Система позволяет формировать отчёты на основе данных базы данных, в том числе:
</p>

<ul>
    <li>доступные и недоступные книги;</li>
    <li>популярные книги;</li>
    <li>просроченные выдачи;</li>
    <li>активные пользователи.</li>
</ul>

<p>
Отчёты могут быть экспортированы в текстовый формат (TXT).
</p>

<hr>

<h2>Безопасность</h2>

<ul>
    <li>Доступ к функциям системы осуществляется после авторизации.</li>
    <li>Функционал ограничен в зависимости от роли пользователя.</li>
    <li>Все операции выдачи и возврата фиксируются в базе данных.</li>
</ul>

<hr>

<p>
При возникновении вопросов по работе системы обратитесь к администратору
или используйте данную справочную страницу.
</p>

